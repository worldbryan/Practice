public class Boss {

    public Integer getBossId() {
        return bossId;
    }

    public void setBossId(Integer bossId) {
        this.bossId = bossId;
    }

    public Integer getBenefit() {
        return benefit;
    }

    public void setBenefit(Integer benefit) {
        this.benefit = benefit;
    }

    public Integer getOwnWorkers() {
        return ownWorkers;
    }

    public void setOwnWorkers(Integer ownWorkers) {
        this.ownWorkers = ownWorkers;
    }

    public Integer getPaySalary() {
        return paySalary;
    }

    public void setPaySalary(Integer paySalary) {
        this.paySalary = paySalary;
    }

    public Integer bossId;
    public Integer benefit;
    public Integer paySalary;
    public Integer ownWorkers;



}
